def make_user_prompt(spec: TopicSpec, topic_index: int, example_index: int) -> str:
    base = spec.user_prompts[example_index % len(spec.user_prompts)]
    context = GLOBAL_CONTEXTS[(example_index + topic_index * 5) % len(GLOBAL_CONTEXTS)]
    prefix_options = ["", "عافاك ", "plz ", "خويا ", "سمح ليا، "]
    suffix_options = [
        "",
        "؟",
        " وبغيت جواب بالدارجة",
        " وراه مقلق بزاف",
        " شنو المعقول؟",
        " واش نجاوبهم؟",
    ]
    prefix = prefix_options[(example_index + topic_index) % len(prefix_options)]
    suffix = suffix_options[(example_index * 2 + topic_index) % len(suffix_options)]

    if example_index % 4 == 0:
        return f"{prefix}{base}، {context}{suffix}".strip()
    if example_index % 4 == 1:
        return f"{prefix}{base}{suffix}".strip()
    if example_index % 4 == 2:
        return f"{base} {context}{suffix}".strip()
    return f"{prefix}{base}، {context}، {suffix}".strip().replace("، ،", "،")


def make_assistant(spec: TopicSpec, topic_index: int, example_index: int) -> str:
    template = spec.assistant_templates[(example_index + topic_index) % len(spec.assistant_templates)]
    return with_disclaimer(template, example_index + topic_index)


def make_record(spec: TopicSpec, topic_index: int, example_index: int) -> dict:
    user = make_user_prompt(spec, topic_index, example_index)
    assistant = make_assistant(spec, topic_index, example_index)
    return {
        "messages": [
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": user},
            {"role": "assistant", "content": assistant},
        ],
        "metadata": {
            "intent": spec.intent,
            "topic": spec.topic,
            "coverage": spec.coverage_label,
            "source": spec.source,
            "requires_rag": spec.requires_rag,
            "quality": "high",
            "advice_like": spec.advice_like,
        },
    }


def normalize_user(text: str) -> str:
    return " ".join(text.split()).casefold()


def build_records() -> list[dict]:
    all_specs = specs()
    counts_by_topic = {spec.topic: 68 for spec in all_specs}
    for spec in all_specs[:4]:
        counts_by_topic[spec.topic] = 69

    records: list[dict] = []
    seen_users: set[str] = set()
    for topic_index, spec in enumerate(all_specs):
        target = counts_by_topic[spec.topic]
        generated = 0
        attempt = 0
        while generated < target:
            record = make_record(spec, topic_index, attempt)
            user_text = record["messages"][1]["content"]
            key = normalize_user(user_text)
            if key in seen_users:
                attempt += 1
                continue
            seen_users.add(key)
            records.append(record)
            generated += 1
            attempt += 1

    if len(records) != TOTAL_EXAMPLES:
        raise RuntimeError(f"expected {TOTAL_EXAMPLES} records, generated {len(records)}")
    return records


def split_records(records: list[dict]) -> dict[str, list[dict]]:
    rng = random.Random(SEED)
    by_topic: dict[str, list[dict]] = {}
    for record in records:
        by_topic.setdefault(record["metadata"]["topic"], []).append(record)

    topic_order = [spec.topic for spec in specs()]
    val_extra_topics = set(topic_order[:18])
    test_extra_topics = set(topic_order[4:])
    splits = {"train": [], "val": [], "test": []}

    for topic in topic_order:
        topic_records = list(by_topic[topic])
        rng.shuffle(topic_records)
        val_n = 6 + (1 if topic in val_extra_topics else 0)
        test_n = 6 + (1 if topic in test_extra_topics else 0)
        val_records = topic_records[:val_n]
        test_records = topic_records[val_n : val_n + test_n]
        train_records = topic_records[val_n + test_n :]
        splits["val"].extend(val_records)
        splits["test"].extend(test_records)
        splits["train"].extend(train_records)

    for split, split_records_list in splits.items():
        rng.shuffle(split_records_list)
        for index, record in enumerate(split_records_list, start=1):
            record["metadata"]["split"] = split
            record["metadata"]["example_id"] = f"lmo7ami-sft-{split}-{index:04d}"

    assert len(splits["train"]) == TRAIN_COUNT
    assert len(splits["val"]) == VAL_COUNT
    assert len(splits["test"]) == TEST_COUNT
    return splits


def write_jsonl(path: Path, rows: list[dict]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="\n") as handle:
        for row in rows:
            handle.write(json.dumps(row, ensure_ascii=False, separators=(",", ":")) + "\n")


def topic_counts(rows: list[dict]) -> dict[str, int]:
    counts: dict[str, int] = {}
    for row in rows:
        topic = row["metadata"]["topic"]
        counts[topic] = counts.get(topic, 0) + 1
    return dict(sorted(counts.items()))


